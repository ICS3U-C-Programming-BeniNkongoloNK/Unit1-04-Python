# Does basic math.


def main():
    print("6-4={}".format(6 - 4))
    print("8+4={}".format(8 + 4))
    print("2*3={}".format(2 * 3))
    print("10/5={}".format(10 / 5))
    print("1/2={}".format(1 / 2))
    print("11+4*5={}".format(11 + 4 * 5))
    print("2^3={}".format(2 ^ 3))  # bitwise operator
    print("2**3={}".format(2**3))  # exponent
    print("16**0.5={}".format(16**0.5))  # square root


if __name__ == "__main__":
    main()
